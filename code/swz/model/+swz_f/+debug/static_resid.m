function [lhs, rhs] = static_resid(y, x, params)
T = NaN(1, 1);
lhs = NaN(10, 1);
rhs = NaN(10, 1);
T(1) = params(2)*params(3)*params(7)/(1-params(2));
lhs(1) = y(1);
rhs(1) = y(1)-(1-params(2))/params(3)*(y(3)-y(2)-y(4));
lhs(2) = y(2);
rhs(2) = y(1)*params(7)*params(8)-T(1)*(params(5)*y(9)+params(6)*y(7))+y(2)*params(1);
lhs(3) = y(3);
rhs(3) = y(3)*params(9)+(1-params(9))*(y(2)*params(14)+y(1)*params(15))+y(10);
lhs(4) = y(4);
rhs(4) = params(3)*(-(1-params(10)))*(1+params(4))/(params(3)+(1-params(2))*params(4))*y(8);
lhs(5) = y(5);
rhs(5) = y(8)*(1-params(2))*(1+params(4))/(params(3)+(1-params(2))*params(4));
lhs(6) = y(7);
rhs(6) = y(7)*params(12)+x(1);
lhs(7) = y(6);
rhs(7) = y(1)+y(5);
lhs(8) = y(8);
rhs(8) = params(10)*y(8)+x(2);
lhs(9) = y(9);
rhs(9) = y(9)*params(11)+x(3);
lhs(10) = y(10);
rhs(10) = y(10)*params(13)+x(4);
end
